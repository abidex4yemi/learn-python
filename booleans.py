############################
# Basic boolean practice   #
############################
print(True)
print(False)

# 0 is falsy
print(bool(0))

# any other number is truthy
print(bool(1))

# all empty container type are falsy
print(bool([]))
print(bool({}))
print(bool(()))
print(bool(set()))

# None type is false
print(bool(None))

# Empty string is falsy
print(bool(""))
